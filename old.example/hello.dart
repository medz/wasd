import 'dart:convert';
import 'dart:typed_data';

import 'package:wasd/wasd.dart';

void main() {
  assert(_helloWat.isNotEmpty);
  final instance = WasmInstance.fromBytes(_helloWasm);
  final pointer = instance.invokeI32('hello_ptr');
  final length = instance.invokeI32('hello_len');
  final bytes = instance.exportedMemory('memory').readBytes(pointer, length);
  print(utf8.decode(bytes));
}

